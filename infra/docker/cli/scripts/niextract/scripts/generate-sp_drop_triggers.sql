SELECT 'DROP PROCEDURE IF EXISTS sp_drop_triggers;\nDELIMITER $$\nCREATE PROCEDURE sp_drop_triggers()\nBEGIN' AS '';

SELECT CONCAT('DROP TRIGGER IF EXISTS ', t.TRIGGER_NAME, ';') AS ''
FROM information_schema.TRIGGERS t
WHERE t.TRIGGER_SCHEMA = DATABASE()
ORDER BY t.TRIGGER_NAME;

SELECT 'END\n$$' AS '';